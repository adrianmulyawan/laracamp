        $data['camp_id'] = $camp->id;

        // Update User Data
        $user = Auth::user();
        $user->email = $data['email'];
        $user->name = $data['name'];
        $user->occupation = $data['occupation'];
        $user->save();

        // Create Checkout Data
        $checkout = Checkout::create($data);

        // return $checkout;

        return redirect()->route('checkout.success');
